require_once "Person.php";

class Administrator extends Person
{
    private $matricule;

    public function __construct()
    {
    }
    public function init($id, $firstname, $lastname, $phone, $mail, $username, $password,$status,$picture,$matricule)
    {
        parent::initadmin($id, $firstname, $lastname, $phone, $mail, $username, $password,$status,$picture); // TODO: Change the autogenerated stub
        $this->matricule=$matricule;
    }


    /**
     * @return mixed
     */
    public function getMatricule()
    {
        return $this->matricule;
    }

    /**
     * @param mixed $matricule
     */
    public function setMatricule($matricule)
    {
        $this->matricule = $matricule;
    }



}